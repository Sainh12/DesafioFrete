public class Carga {

    //=============================Atributos=================================
    private String descricao;
    private double massa;

    //=========================Getters and Setters===========================
    public void setDescricao(String descricao){
        this.descricao=descricao;
    }
    public String getDescricao(){
        return descricao;
    }
    public void setMassa(double massa){
        this.massa=massa;
    }
    public double getMassa(){
        return massa;
    }

    //======================================================================
}
